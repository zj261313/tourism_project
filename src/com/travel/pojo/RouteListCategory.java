package com.travel.pojo;

import java.io.Serializable;

public class RouteListCategory implements Serializable {

    private int cid;//分类id
    private String cname;//分类名称

    public RouteListCategory() {
    }

    public RouteListCategory(int cid, String cname) {
        this.cid = cid;
        this.cname = cname;
    }

    @Override
    public String toString() {
        return "Category{" +
                "cid=" + cid +
                ", cname='" + cname + '\'' +
                '}';
    }

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getCname() {
        return cname;
    }

    public void setCname(String cname) {
        this.cname = cname;
    }
}
